    </p>
    <ol>
      <li><strong>飲み方を決める</strong>——ショットやカクテルで楽しむのか、ストレートでじっくり味わうのか。前者ならブランコ、後者ならレポサド以上の熟成クラスが基本の選択肢になります。</li>
      <li><strong>100%アガベ表記を確認する</strong>——ラベルに「100% de agave」とあるかをチェック。風味を重視するなら100%アガベ、コスパと気軽さ重視ならミクストという判断基準で選びます。</li>
      <li><strong>予算で熟成クラスを絞る</strong>——3,000円前後ならブランコ、5,000円前後ならレポサド、8,000円以上出せるならアネホと、予算と熟成期間はおおむね比例します。</li>
      <li><strong>香りの好みで方向性を選ぶ</strong>——柑橘系の爽やかさが好きならブランコ、バニラやカラメルの甘い香りが好きならアネホ寄り。ウイスキー好きの方は熟成クラスから入ると馴染みやすいでしょう。</li>
      <li><strong>NOM番号と輸入元を確認して購入する</strong>——正規のNOM番号があるか、国内の信頼できる輸入元が扱っているかを最後に確認すれば、品質面での失敗はほぼ防げます。</li>
    </ol>

    <h2>シーン別・レベル別のおすすめの楽しみ方</h2>
    <h3>初心者は「ブランコ→レポサド→アネホ」の順で飲み比べ</h3>
