    <h2>ラベルの読み方——NOM番号と「100% de agave」をチェック</h2>
    <p>
      種類の知識を実際のボトル選びに活かすには、ラベルの読み方を押さえておくことが欠かせません。チェックすべきポイントは主に3つあります。
    </p>
    <ul>
      <li><strong>クラス表記</strong>——Blanco / Joven / Reposado / Añejo / Extra Añejo のいずれかが記載されています。これが熟成期間の証明です。</li>
      <li><strong>「100% de agave」表記</strong>——この一文があれば100%アガベテキーラ。なければミクストです。「100% puro de agave」と書かれることもあります。</li>
      <li><strong>NOM番号</strong>——「NOM-1234」のような4桁の数字は、メキシコ政府が認証した蒸留所の登録番号です。この番号がある一本は、規格を満たした正規のテキーラであることを意味します。</li>
    </ul>
    <p>
      NOM番号は蒸留所ごとに割り当てられているため、番号を調べればどの蒸留所で造られたかまでわかります。同じNOM番号を持つ別ブランドを探して飲み比べる、といったマニアックな楽しみ方ができるのもテキーラならではです。逆に、これらの表記が一切ない「テキーラ風」のお酒は正規のテキーラではない可能性があるので注意しましょう。
    </p>

    <h2>失敗しないテキーラの選び方5ステップ</h2>
    <p>
