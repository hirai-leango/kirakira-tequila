    <h2>テキーラの原点——アステカ時代の「プルケ」</h2>
    <p>
      テキーラの物語は、蒸留酒が生まれるはるか昔、メキシコ先住民の時代にまでさかのぼります。メキシコの乾いた大地には、アガベ（リュウゼツラン）と呼ばれる多肉植物が自生していました。剣のように鋭い葉を放射状に広げるこの植物は、先住民にとって特別な存在であり、食料、繊維、建材、そして飲み物の原料として、生活のあらゆる場面で活用されてきました。
    </p>
    <p>
      アステカ文明の時代、人々はアガベの樹液を自然発酵させた「プルケ」と呼ばれる醸造酒を造っていました。白く濁り、とろみのあるプルケは、単なる嗜好品ではなく、神々に捧げる神聖な飲み物とされ、宗教儀式の場で振る舞われていました。アステカ神話には、マヤウェルというアガベの女神が登場します。アガベが「神々の植物」として崇められていたことは、後のテキーラ文化にも深く受け継がれていくことになります。
    </p>
