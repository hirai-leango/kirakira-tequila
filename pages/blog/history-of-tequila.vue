    </p>

    <h2>蒸留技術の伝来——スペイン征服とメスカルの誕生</h2>
    <p>
      転機が訪れたのは16世紀。1521年、スペインのコンキスタドール（征服者）エルナン・コルテスがアステカ帝国を征服し、メキシコはスペインの植民地となります。ヨーロッパから持ち込まれたワインやブランデーが不足すると、スペイン人たちは現地の材料で酒を造ることを考えました。そこで彼らが持ち込んだ蒸留技術と、先住民が育ててきたアガベ文化が出会います。
    </p>
    <p>
      アガベの茎（ピニャ）を加熱して糖化させ、発酵させたもろみを蒸留する——こうして生まれたのが「メスカル」と呼ばれる蒸留酒です。メスカルはメキシコ各地で造られるようになり、地域ごとに個性豊かな味わいが育まれていきました。その中で、ハリスコ州のテキーラ村周辺で造られるメスカルは、この土地特有のブルーアガベ（アガベ・アスール・テキラーナ）を使うことで、ひときわ評判の高い酒として知られるようになります。
    </p>
    <p>
      「テキーラ」という名前は、まさにこの村の名前に由来します。フランスのシャンパーニュ地方で造られる発泡ワインだけが「シャンパン」を名乗れるように、テキーラもまた、<strong>土地の名前を冠した酒</strong>なのです。
    </p>

    <h2>テキーラ産業の確立——18〜19世紀</h2>
    <p>
      18世紀に入ると、テキーラ村周辺のメスカル造りは本格的な産業へと発展していきます。1758年には、後に世界最大のテキーラメーカーとなるホセ・クエルボ家がアガベの栽培を開始。1795年には、スペイン王室から正式な製造許可を取得し、商業的なテキーラ生産の幕が開きました。
    </p>
    <p>
      19世紀には蒸留所が次々と設立され、「テキーラ地方のメスカル」は次第に「テキーラ」という独立した名前で呼ばれるようになります。鉄道の開通により、テキーラはメキシコ全土、さらにはアメリカへと販路を広げていきました。1873年には初めてアメリカへの正式な輸出が記録されており、これが国際的な酒としてのテキーラの第一歩となりました。
    </p>

    <h2>革命とともに——メキシコの国民的アイデンティティへ</h2>
    <p>
      20世紀初頭のメキシコ革命（1910年〜）は、テキーラの文化的な地位を大きく押し上げました。革命の時代、テキーラは民衆の酒、そしてメキシコ人の誇りの象徴として愛されるようになります。革命後の国民意識の高まりの中で、ヨーロッパ由来の酒ではなく、メキシコの大地から生まれたテキーラこそが「我らの酒」だという意識が定着していきました。
    </p>
    <p>
      さらに1940〜50年代、メキシコ映画の黄金期には、銀幕のスターたちがテキーラを酌み交わす姿が繰り返し描かれ、マリアッチの音楽とともに、テキーラはメキシコ文化そのものを象徴する存在となります。第二次世界大戦中には、ヨーロッパからの酒類輸入が途絶えたアメリカでテキーラの需要が急増し、国際市場での地位も確固たるものになりました。
    </p>

    <h2>原産地呼称制度——「本物のテキーラ」を守る仕組み</h2>
    <p>
      テキーラの品質と名声を守るため、メキシコ政府は法整備を進めてきました。1974年、テキーラは原産地呼称（DO：Denominación de Origen）として保護され、「テキーラ」と名乗れる酒の条件が法的に定められました。
    </p>
    <ul>
      <li>ハリスコ州全域および周辺4州の指定地域で製造されていること</li>
      <li>原料にブルーアガベ（アガベ・アスール・テキラーナ・ウェーバー）を使用していること</li>
      <li>アガベ由来の糖分を51%以上使用していること（100%のものは「100%アガベ」表記が可能）</li>
      <li>規定のアルコール度数など、公式規格（NOM）を満たしていること</li>
    </ul>
    <p>
      1994年にはテキーラ規制委員会（CRT）が設立され、栽培から瓶詰めまでの全工程を監督しています。ボトルに記載される「NOM」の番号は、その一本が正規の蒸留所で造られた証です。こうした厳格な制度があるからこそ、世界中の人々が安心して「本物のテキーラ」を楽しめるのです。
    </p>

    <h2>テキーラの種類と熟成の文化</h2>
    <p>
      テキーラは熟成期間によって呼び名が変わり、それぞれ異なる魅力を持っています。
    </p>
    <ul>
      <li><strong>ブランコ（シルバー）</strong>——熟成させない、あるいは60日未満の若いテキーラ。アガベ本来のフレッシュで力強い風味が特徴で、ショットやカクテルに最適です。</li>
      <li><strong>レポサド</strong>——樽で2ヶ月以上1年未満熟成。「休ませた」という意味の名の通り、樽由来のまろやかさとアガベの風味が調和します。</li>
      <li><strong>アネホ</strong>——1年以上3年未満の熟成。琥珀色に色づき、バニラやカラメルを思わせる深い味わいで、ゆっくり味わうのに向いています。</li>
      <li><strong>エクストラ・アネホ</strong>——3年以上熟成させた最高級クラス。ウイスキーやブランデーにも匹敵する複雑で豊かな味わいを持ちます。</li>
    </ul>
    <p>
      かつて「安くて強い酒」というイメージで語られがちだったテキーラですが、2000年代以降はプレミアムテキーラのブームが世界的に巻き起こり、100%アガベの高品質なテキーラをじっくり味わう文化が広がっています。セレブリティが自らテキーラブランドを立ち上げる例も相次ぎ、テキーラは今や<strong>世界で最も注目されるスピリッツのひとつ</strong>となりました。
    </p>

    <h2>ユネスコ世界遺産に登録されたアガベの景観</h2>
    <p>
      2006年、「テキーラ村の古い産業施設群とアガベの景観」はユネスコの世界文化遺産に登録されました。青みがかったアガベ畑が地平線まで続く風景は「アガベ・ブルー」と称され、世界中の旅行者を魅了しています。畑でアガベを収穫する職人「ヒマドール」の伝統技術も、親から子へと受け継がれる貴重な文化です。
    </p>
    <p>
      アガベは植え付けから収穫まで5年以上、長いものでは10年近くかかります。一本のテキーラの背後には、それだけの歳月と人の手仕事があるのです。テキーラが単なるアルコールではなく「文化」と呼ばれる理由が、ここにあります。
    </p>

    <h2>日本におけるテキーラ文化</h2>
    <p>
      日本でテキーラが広く知られるようになったのは、1970年代以降のこと。当初はカクテル「マルガリータ」や「テキーラサンライズ」の材料として親しまれ、バブル期にはディスコやバーでのショット文化とともに浸透していきました。
    </p>
    <p>
      そして現代、テキーラの新しい主戦場となっているのが、コンセプトカフェやガールズバーといったナイトシーンです。乾杯の掛け声とともに交わされるテキーラショットは、お店とお客様の距離を縮める、コミュニケーションの象徴になっています。日本テキーラ協会の活動やプレミアムテキーラの輸入増加もあり、「安い罰ゲームの酒」から「みんなで楽しむ特別な一杯」へと、テキーラのイメージは着実に進化しています。
    </p>

    <h2>ショット文化と「特別な一杯」のこれから</h2>
    <p>
      塩を舐め、ショットを飲み干し、ライムをかじる——おなじみのスタイルは、メキシコの伝統的な飲み方「トラゴ」に由来すると言われます。仲間と目を合わせ、グラスを掲げ、同じ瞬間を共有する。テキーラショットの本質は、アルコールそのものではなく、<strong>その場の一体感と高揚感</strong>にあります。
    </p>
    <p>
      だからこそ、私たちキラキラテキーラは考えました。その特別な瞬間に相応しい、特別な輝きを纏った一杯があってもいいのではないか、と。ダイヤモンドをあしらったボトルと、光を乱反射する特殊カットのグラス。数百年の歴史を持つテキーラ文化に、日本の夜の街から新しい1ページを加えること。それが私たちの願いです。
    </p>

    <h2>まとめ——歴史を知れば、一杯がもっと美味しくなる</h2>
    <p>
      アステカの神々に捧げられたプルケから始まり、スペインの蒸留技術との出会い、テキーラ村での産業化、原産地呼称による品質保護、そして世界的なプレミアムブーム。テキーラの歴史は、メキシコという国の歴史そのものであり、人と人が集う場の歴史でもあります。
    </p>
