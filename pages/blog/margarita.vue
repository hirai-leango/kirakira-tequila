      <li><strong>ライムは生を搾る</strong>——市販のライム果汁でも作れますが、搾りたての香りは別物。ライム1/2個で約15mlが目安です。</li>
      <li><strong>計量を省略しない</strong>——ショートカクテルは総量60mlの世界。5mlの誤差が味を大きく変えます。</li>
      <li><strong>氷はケチらない</strong>——氷が少ないと冷える前に溶けて水っぽくなります。大きめの氷をたっぷりが鉄則。</li>
      <li><strong>グラスは必ず冷やす</strong>——常温のグラスに注ぐと、せっかくシェイクで冷やした意味が半減します。</li>
      <li><strong>塩は外側だけに付ける</strong>——内側に付いた塩は飲むほどにカクテルへ落ち、味を崩します。</li>
    </ul>
    <p>
      この5点を守るだけで、自宅のマルガリータは見違えるほどお店の味に近づきます。
    </p>

    <h2>居酒屋・バー・ナイトシーンでのマルガリータの楽しみ方</h2>
    <p>
      マルガリータは自宅だけでなく、外で飲むシーンでも主役になれるカクテルです。オーセンティックバーなら、バーテンダーに「テキーラはブランコで」「塩はハーフで」など好みを伝えてみましょう。一杯ごとの違いを楽しめるのがバーの醍醐味です。メキシコ料理店や居酒屋では、フローズンマルガリータとタコスの組み合わせが鉄板。塩気と酸味が油分をリセットしてくれるので、食中酒としても優秀です。
    </p>
    <p>
      そしてコンセプトカフェやガールズバーといったナイトシーンでは、テキーラは乾杯の主役。ショットだけでなく、マルガリータのようなカクテルを挟むことで、ペースを保ちながら長く楽しめます。近年は乾杯の瞬間を華やかに演出するアイテムも人気で、ダイヤモンドをあしらった光るボトルの<strong>キラキラテキーラ</strong>のように、テキーラそのものをショーアップするブランドも夜の街で注目を集めています。マルガリータの物語性と、輝くボトルの演出——テキーラは「味わう酒」であると同時に「場を盛り上げる酒」でもあるのです。
    </p>
    <p>
      ただし、楽しむうえで節度は絶対条件です。<strong>20歳未満の飲酒は法律で禁止されています。</strong>マルガリータは飲みやすい一方でアルコール度数25度前後としっかり強いカクテルですから、一気飲みは避け、チェイサー（水）を挟みながら自分のペースで味わいましょう。飲酒運転は言うまでもなく厳禁です。
    </p>

