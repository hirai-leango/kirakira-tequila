    </p>
    <p>
      そこでピニャを石窯やオートクレーブでじっくりと蒸し焼きにします。すると熱の作用でイヌリンが分解され、酵母が食べられる<strong>果糖などの単純な糖</strong>へと変化します。これが糖化です。加熱を終えたピニャはねっとりと甘く、焼き芋のような香ばしい風味をまといます。この糖化があってこそ、アガベはテキーラへと生まれ変われるのです。加熱方法（伝統的な石窯か近代的なオートクレーブか）によって風味も変わってきます。
    </p>

