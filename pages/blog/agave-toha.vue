    <ol>
      <li><strong>栽培</strong>——ブルーアガベを植え付け、5〜10年かけて糖分を茎に蓄えさせる。</li>
      <li><strong>ヒマ（収穫）</strong>——ヒマドールがコアで葉を落とし、糖度が最高潮に達したピニャを掘り出す。</li>
      <li><strong>加熱</strong>——ピニャを石窯やオートクレーブで蒸し焼きにし、硬い炭水化物イヌリンを甘い糖分へと変える。</li>
      <li><strong>搾汁</strong>——加熱してやわらかくなったピニャを砕いて圧搾し、甘い果汁（アガベジュース）を絞り出す。</li>
      <li><strong>発酵</strong>——搾った果汁に酵母を加え、糖分をアルコールに変えてもろみ（発酵液）を造る。</li>
      <li><strong>蒸留</strong>——もろみを2回蒸留してアルコール分と香味を凝縮させ、テキーラの原酒が完成する。</li>
    </ol>

    <h3>加熱による糖化——イヌリンから果糖へ</h3>
    <p>
